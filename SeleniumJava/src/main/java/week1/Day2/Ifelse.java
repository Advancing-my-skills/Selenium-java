package week1.Day2;

public class Ifelse {

	public static void main(String[] args) {
		int mark = 50;
		if(mark>=80) {
			System.out.println("A");
		}
		else if(mark>=70) {
			System.out.println("B");
		}
		else if(mark>=60) {
			System.out.println("C");
		}
		else if(mark>=50) {
			System.out.println("D");
		}
		else{
            System.out.println("FAIL!");
	}

}}
